<?php

//Operadores de Comparação

$nunA = 10.00;
$nunB = 10;

var_dump($nunA > $nunB); //Comparando se um número é maior que o outro
echo "<br>";
var_dump($nunA < $nunB); //Comparando se um número é menor que o outro
echo "<br>";
var_dump($nunA == $nunB); //Comparando Igualdade de valores
echo "<br>";
var_dump($nunA === $nunB); //Comparando Igualdade de tipos
echo "<br>";
var_dump($nunA != $nunB); //Verificando se um número é diferente do outro
echo "<br>";
var_dump($nunA !== $nunB); //Verificando se um número e o tipo são diferentes
echo "<br>";

?>